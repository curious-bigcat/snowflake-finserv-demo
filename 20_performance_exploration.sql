-- ============================================================
-- 9. RESOURCE MONITORS
--    Set credit guardrails on warehouses.
-- ============================================================

CREATE OR REPLACE RESOURCE MONITOR FINSERV_MONITOR
    WITH CREDIT_QUOTA = 10
    FREQUENCY = MONTHLY
    START_TIMESTAMP = IMMEDIATELY
    TRIGGERS
        ON 80 PERCENT DO NOTIFY
        ON 95 PERCENT DO SUSPEND
        ON 100 PERCENT DO SUSPEND_IMMEDIATE;

ALTER WAREHOUSE FINSERV_WH SET RESOURCE_MONITOR = FINSERV_MONITOR;

SHOW RESOURCE MONITORS;


-- ============================================================
-- 10. PERFORMANCE SUMMARY TABLE
--     Capture key metrics for comparison.
-- ============================================================

CREATE OR REPLACE TABLE CONSUMPTION.PERFORMANCE_SUMMARY AS
SELECT
    QUERY_ID,
    QUERY_TEXT,
    WAREHOUSE_NAME,
    WAREHOUSE_SIZE,
    EXECUTION_TIME AS EXEC_MS,
    COMPILATION_TIME AS COMPILE_MS,
    BYTES_SCANNED,
    ROWS_PRODUCED,
    PARTITIONS_SCANNED,
    PARTITIONS_TOTAL,
    ROUND(PARTITIONS_SCANNED / NULLIF(PARTITIONS_TOTAL, 0) * 100, 2) AS PARTITION_SCAN_PCT,
    BYTES_SPILLED_TO_LOCAL_STORAGE AS LOCAL_SPILL,
    BYTES_SPILLED_TO_REMOTE_STORAGE AS REMOTE_SPILL,
    PERCENTAGE_SCANNED_FROM_CACHE AS CACHE_HIT_PCT,
    QUEUED_OVERLOAD_TIME AS QUEUE_MS,
    START_TIME
FROM SNOWFLAKE.ACCOUNT_USAGE.QUERY_HISTORY
WHERE WAREHOUSE_NAME IN ('FINSERV_WH', 'FINSERV_WH_SMALL')
  AND START_TIME >= DATEADD('hour', -1, CURRENT_TIMESTAMP())
  AND QUERY_TYPE = 'SELECT'
ORDER BY START_TIME DESC;

SELECT * FROM CONSUMPTION.PERFORMANCE_SUMMARY
ORDER BY START_TIME DESC
LIMIT 20;

