-- ============================================================
-- 1. EXPLAIN PLANS & QUERY PROFILING
--    Read execution plans before running expensive queries.
-- ============================================================

EXPLAIN USING TABULAR
SELECT
    cp.CUSTOMER_ID, cp.FIRST_NAME, cp.LAST_NAME,
    cp.TOTAL_BALANCE, cp.LATEST_RISK_SCORE,
    te.TXN_COUNT, te.TOTAL_SPENT
FROM CURATED.DT_CUSTOMER_PROFILE cp
LEFT JOIN (
    SELECT CUSTOMER_ID, COUNT(*) AS TXN_COUNT, SUM(AMOUNT) AS TOTAL_SPENT
    FROM CURATED.DT_TRANSACTION_ENRICHED
    GROUP BY CUSTOMER_ID
) te ON cp.CUSTOMER_ID = te.CUSTOMER_ID
WHERE cp.TOTAL_BALANCE > 100000;

EXPLAIN USING JSON
SELECT CATEGORY, CHANNEL, COUNT(*) AS TXN_COUNT, SUM(AMOUNT) AS TOTAL_AMOUNT
FROM BASE.TRANSACTIONS
GROUP BY CATEGORY, CHANNEL
ORDER BY TOTAL_AMOUNT DESC;


-- ============================================================
-- 2. WAREHOUSE SIZING & SCALING
--    Compare warehouse sizes and understand multi-cluster.
-- ============================================================

SHOW WAREHOUSES LIKE 'FINSERV%';

SELECT
    WAREHOUSE_NAME, WAREHOUSE_SIZE, WAREHOUSE_TYPE,
    AUTO_SUSPEND, AUTO_RESUME, MIN_CLUSTER_COUNT, MAX_CLUSTER_COUNT,
    SCALING_POLICY
FROM SNOWFLAKE.ACCOUNT_USAGE.WAREHOUSES
WHERE WAREHOUSE_NAME LIKE 'FINSERV%'
  AND DELETED IS NULL;

CREATE WAREHOUSE IF NOT EXISTS FINSERV_WH_SMALL
    WAREHOUSE_SIZE  = 'SMALL'
    AUTO_SUSPEND    = 60
    AUTO_RESUME     = TRUE
    INITIALLY_SUSPENDED = TRUE
    COMMENT         = 'Performance test — SMALL warehouse';

ALTER WAREHOUSE FINSERV_WH_SMALL RESUME IF SUSPENDED;

USE WAREHOUSE FINSERV_WH_SMALL;
SELECT COUNT(*) AS ROW_COUNT,
       SUM(AMOUNT) AS TOTAL,
       AVG(AMOUNT) AS AVG_AMT
FROM BASE.TRANSACTIONS t
JOIN BASE.ACCOUNTS a ON t.ACCOUNT_ID = a.ACCOUNT_ID
JOIN BASE.CUSTOMERS c ON a.CUSTOMER_ID = c.CUSTOMER_ID;

USE WAREHOUSE FINSERV_WH;
SELECT COUNT(*) AS ROW_COUNT,
       SUM(AMOUNT) AS TOTAL,
       AVG(AMOUNT) AS AVG_AMT
FROM BASE.TRANSACTIONS t
JOIN BASE.ACCOUNTS a ON t.ACCOUNT_ID = a.ACCOUNT_ID
JOIN BASE.CUSTOMERS c ON a.CUSTOMER_ID = c.CUSTOMER_ID;

SELECT
    QUERY_ID, WAREHOUSE_NAME, WAREHOUSE_SIZE,
    EXECUTION_TIME, COMPILATION_TIME,
    BYTES_SCANNED, ROWS_PRODUCED,
    PARTITIONS_SCANNED, PARTITIONS_TOTAL,
    BYTES_SPILLED_TO_LOCAL_STORAGE,
    BYTES_SPILLED_TO_REMOTE_STORAGE,
    QUERY_TEXT
FROM SNOWFLAKE.ACCOUNT_USAGE.QUERY_HISTORY
WHERE WAREHOUSE_NAME IN ('FINSERV_WH', 'FINSERV_WH_SMALL')
  AND START_TIME >= DATEADD('minute', -10, CURRENT_TIMESTAMP())
  AND QUERY_TYPE = 'SELECT'
ORDER BY START_TIME DESC
LIMIT 10;

ALTER WAREHOUSE FINSERV_WH_SMALL SUSPEND;


-- ============================================================
-- 3. MICRO-PARTITIONS & CLUSTERING METADATA
--    Inspect how data is physically organized.
-- ============================================================

SELECT SYSTEM$CLUSTERING_INFORMATION('BASE.TRANSACTIONS', '(ACCOUNT_ID)') AS CLUSTERING_INFO;

SELECT SYSTEM$CLUSTERING_INFORMATION('BASE.TRANSACTIONS', '(TXN_DATE)') AS CLUSTERING_INFO;

SELECT SYSTEM$CLUSTERING_INFORMATION('BASE.TRANSACTIONS', '(CATEGORY)') AS CLUSTERING_INFO;

SELECT
    TABLE_NAME,
    ROW_COUNT,
    BYTES,
    ROUND(BYTES / POWER(1024, 2), 2) AS SIZE_MB,
    CLUSTERING_KEY
FROM INFORMATION_SCHEMA.TABLES
WHERE TABLE_SCHEMA = 'BASE'
  AND TABLE_TYPE = 'BASE TABLE'
ORDER BY BYTES DESC;


-- ============================================================
-- 4. AUTOMATIC CLUSTERING
--    Add clustering keys and monitor auto-clustering.
-- ============================================================

CREATE TABLE IF NOT EXISTS BASE.TRANSACTIONS_CLUSTERED
    CLUSTER BY (CATEGORY, CHANNEL)
AS SELECT * FROM BASE.TRANSACTIONS;

SELECT SYSTEM$CLUSTERING_INFORMATION('BASE.TRANSACTIONS_CLUSTERED', '(CATEGORY, CHANNEL)') AS AFTER_CLUSTER;

SELECT * FROM TABLE(INFORMATION_SCHEMA.AUTOMATIC_CLUSTERING_HISTORY(
    DATE_RANGE_START => DATEADD('hour', -24, CURRENT_TIMESTAMP()),
    TABLE_NAME       => 'FINSERV_DB.BASE.TRANSACTIONS_CLUSTERED'
));

SELECT
    'UNCLUSTERED' AS VERSION,
    PARSE_JSON(SYSTEM$CLUSTERING_INFORMATION('BASE.TRANSACTIONS', '(CATEGORY, CHANNEL)')):average_overlaps::FLOAT AS AVG_OVERLAP,
    PARSE_JSON(SYSTEM$CLUSTERING_INFORMATION('BASE.TRANSACTIONS', '(CATEGORY, CHANNEL)')):average_depth::FLOAT AS AVG_DEPTH
UNION ALL
SELECT
    'CLUSTERED',
    PARSE_JSON(SYSTEM$CLUSTERING_INFORMATION('BASE.TRANSACTIONS_CLUSTERED', '(CATEGORY, CHANNEL)')):average_overlaps::FLOAT,
    PARSE_JSON(SYSTEM$CLUSTERING_INFORMATION('BASE.TRANSACTIONS_CLUSTERED', '(CATEGORY, CHANNEL)')):average_depth::FLOAT;


