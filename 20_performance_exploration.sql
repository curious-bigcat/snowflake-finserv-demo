-- ============================================================
-- 7. WAREHOUSE UTILIZATION & SPILLING
--    Monitor load, queuing, and disk spill.
-- ============================================================

SELECT *
FROM TABLE(INFORMATION_SCHEMA.WAREHOUSE_LOAD_HISTORY(
    DATE_RANGE_START => DATEADD('hour', -2, CURRENT_TIMESTAMP()),
    WAREHOUSE_NAME   => 'FINSERV_WH'
))
ORDER BY START_TIME DESC
LIMIT 20;

SELECT
    WAREHOUSE_NAME,
    COUNT(*) AS QUERY_COUNT,
    AVG(EXECUTION_TIME) AS AVG_EXEC_MS,
    AVG(QUEUED_OVERLOAD_TIME) AS AVG_QUEUE_MS,
    SUM(BYTES_SPILLED_TO_LOCAL_STORAGE) AS TOTAL_LOCAL_SPILL,
    SUM(BYTES_SPILLED_TO_REMOTE_STORAGE) AS TOTAL_REMOTE_SPILL,
    AVG(PARTITIONS_SCANNED) AS AVG_PARTITIONS_SCANNED,
    AVG(PARTITIONS_TOTAL) AS AVG_PARTITIONS_TOTAL,
    ROUND(AVG(PARTITIONS_SCANNED) / NULLIF(AVG(PARTITIONS_TOTAL), 0) * 100, 2) AS PARTITION_SCAN_PCT
FROM SNOWFLAKE.ACCOUNT_USAGE.QUERY_HISTORY
WHERE WAREHOUSE_NAME = 'FINSERV_WH'
  AND START_TIME >= DATEADD('hour', -2, CURRENT_TIMESTAMP())
  AND QUERY_TYPE = 'SELECT'
GROUP BY WAREHOUSE_NAME;


-- ============================================================
-- 8. QUERY ACCELERATION SERVICE
--    Offload portions of large scans to shared compute.
