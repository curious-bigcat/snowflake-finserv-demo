SELECT SYSTEM$ESTIMATE_QUERY_ACCELERATION('FINSERV_WH') AS QAS_ESTIMATE;

SELECT
    QUERY_ID, ELIGIBLE_QUERY_ACCELERATION_TIME, UPPER_LIMIT_SCALE_FACTOR
FROM SNOWFLAKE.ACCOUNT_USAGE.QUERY_ACCELERATION_ELIGIBLE
WHERE WAREHOUSE_NAME = 'FINSERV_WH'
  AND START_TIME >= DATEADD('hour', -24, CURRENT_TIMESTAMP())
