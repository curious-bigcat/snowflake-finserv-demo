SELECT '>>> SAMPLE: Channel Performance <<<' AS SECTION;
SELECT CHANNEL,
       SUM(TRANSACTION_COUNT) AS TOTAL_TXNS,
       ROUND(SUM(TOTAL_VOLUME), 2) AS TOTAL_VOLUME,
       ROUND(AVG(AVG_AMOUNT), 2) AS AVG_TXN
FROM CONSUMPTION.DT_CHANNEL_PERFORMANCE
GROUP BY CHANNEL
ORDER BY TOTAL_VOLUME DESC;

