-- ============================================================
-- 6. AGGREGATION POLICY
--    Prevent individual-level data extraction by analysts.
--    Non-admin roles must GROUP BY with MIN_GROUP_SIZE >= 5.
-- ============================================================

SELECT '>>> AGGREGATION POLICY <<<' AS SECTION;

CREATE OR REPLACE AGGREGATION POLICY GOVERNANCE.FINSERV_AGGREGATION_POLICY
    AS () RETURNS AGGREGATION_CONSTRAINT ->
    CASE
        WHEN IS_ROLE_IN_SESSION('ACCOUNTADMIN') THEN NO_AGGREGATION_CONSTRAINT()
        WHEN IS_ROLE_IN_SESSION('FINSERV_ADMIN') THEN NO_AGGREGATION_CONSTRAINT()
        WHEN IS_ROLE_IN_SESSION('FINSERV_COMPLIANCE_OFFICER') THEN NO_AGGREGATION_CONSTRAINT()
        ELSE AGGREGATION_CONSTRAINT(MIN_GROUP_SIZE => 5)
    END;

-- Apply to CUSTOMERS — analysts must aggregate, no single-row extraction
ALTER TABLE BASE.CUSTOMERS
    SET AGGREGATION POLICY GOVERNANCE.FINSERV_AGGREGATION_POLICY;

SELECT 'Aggregation policy applied to CUSTOMERS (min group size = 5)' AS STATUS;

