-- ============================================================
-- 4. TAG-BASED MASKING POLICIES
--    Attach masking to the DATA_CLASSIFICATION tag so any
--    future column tagged PII/RESTRICTED is auto-protected.
-- ============================================================

SELECT '>>> TAG-BASED MASKING <<<' AS SECTION;

-- Tag-based masking: VARCHAR columns tagged with DATA_CLASSIFICATION
CREATE OR REPLACE MASKING POLICY GOVERNANCE.TAG_MASK_STRING
    AS (val VARCHAR) RETURNS VARCHAR ->
    CASE
        WHEN IS_ROLE_IN_SESSION('ACCOUNTADMIN') THEN val
        WHEN IS_ROLE_IN_SESSION('FINSERV_ADMIN') THEN val
        WHEN IS_ROLE_IN_SESSION('FINSERV_COMPLIANCE_OFFICER') THEN val
        WHEN SYSTEM$GET_TAG_ON_CURRENT_COLUMN('GOVERNANCE.DATA_CLASSIFICATION') = 'PUBLIC' THEN val
        WHEN SYSTEM$GET_TAG_ON_CURRENT_COLUMN('GOVERNANCE.DATA_CLASSIFICATION') = 'INTERNAL' THEN val
        WHEN SYSTEM$GET_TAG_ON_CURRENT_COLUMN('GOVERNANCE.DATA_CLASSIFICATION') IN ('PII', 'RESTRICTED')
            THEN '***MASKED***'
        WHEN SYSTEM$GET_TAG_ON_CURRENT_COLUMN('GOVERNANCE.DATA_CLASSIFICATION') = 'SENSITIVE'
            THEN LEFT(val, 1) || '***'
        ELSE val
    END;

-- Tag-based masking: NUMBER columns
CREATE OR REPLACE MASKING POLICY GOVERNANCE.TAG_MASK_NUMBER
    AS (val NUMBER) RETURNS NUMBER ->
    CASE
        WHEN IS_ROLE_IN_SESSION('ACCOUNTADMIN') THEN val
        WHEN IS_ROLE_IN_SESSION('FINSERV_ADMIN') THEN val
        WHEN IS_ROLE_IN_SESSION('FINSERV_COMPLIANCE_OFFICER') THEN val
        WHEN SYSTEM$GET_TAG_ON_CURRENT_COLUMN('GOVERNANCE.DATA_CLASSIFICATION') IN ('PUBLIC', 'INTERNAL') THEN val
        WHEN SYSTEM$GET_TAG_ON_CURRENT_COLUMN('GOVERNANCE.DATA_CLASSIFICATION') IN ('PII', 'RESTRICTED', 'SENSITIVE')
            THEN -1
        ELSE val
    END;

-- Tag-based masking: DATE columns
CREATE OR REPLACE MASKING POLICY GOVERNANCE.TAG_MASK_DATE
    AS (val DATE) RETURNS DATE ->
    CASE
        WHEN IS_ROLE_IN_SESSION('ACCOUNTADMIN') THEN val
        WHEN IS_ROLE_IN_SESSION('FINSERV_ADMIN') THEN val
        WHEN IS_ROLE_IN_SESSION('FINSERV_COMPLIANCE_OFFICER') THEN val
        WHEN SYSTEM$GET_TAG_ON_CURRENT_COLUMN('GOVERNANCE.DATA_CLASSIFICATION') IN ('PII', 'RESTRICTED')
            THEN DATE_FROM_PARTS(YEAR(val), 1, 1)
        ELSE val
    END;

-- Tag-based masking: VARIANT columns
CREATE OR REPLACE MASKING POLICY GOVERNANCE.TAG_MASK_VARIANT
    AS (val VARIANT) RETURNS VARIANT ->
    CASE
        WHEN IS_ROLE_IN_SESSION('ACCOUNTADMIN') THEN val
        WHEN IS_ROLE_IN_SESSION('FINSERV_ADMIN') THEN val
        WHEN IS_ROLE_IN_SESSION('FINSERV_COMPLIANCE_OFFICER') THEN val
        WHEN SYSTEM$GET_TAG_ON_CURRENT_COLUMN('GOVERNANCE.DATA_CLASSIFICATION') IN ('PII', 'RESTRICTED', 'SENSITIVE')
            THEN PARSE_JSON('{"masked": true}')
        ELSE val
    END;

-- Attach masking policies to the DATA_CLASSIFICATION tag
-- NOTE: Tag-based masking and column-level masking are mutually exclusive.
-- The column-level policies from file 21 take precedence on those columns.
-- Tag-based policies protect NEW columns tagged in the future automatically.
ALTER TAG GOVERNANCE.DATA_CLASSIFICATION SET
    MASKING POLICY GOVERNANCE.TAG_MASK_STRING,
    MASKING POLICY GOVERNANCE.TAG_MASK_NUMBER,
    MASKING POLICY GOVERNANCE.TAG_MASK_DATE,
    MASKING POLICY GOVERNANCE.TAG_MASK_VARIANT;

SELECT 'Tag-based masking policies attached to DATA_CLASSIFICATION tag' AS STATUS;


