-- ============================================================
-- 8. DATA METRIC FUNCTIONS (DMFs)
--    Automated quality checks tied to governance.
-- ============================================================

SELECT '>>> DATA METRIC FUNCTIONS <<<' AS SECTION;

-- DMF: NULL rate for CUSTOMER_ID foreign key in TRANSACTIONS
CREATE OR REPLACE DATA METRIC FUNCTION GOVERNANCE.DMF_NULL_ACCOUNT_ID(
    ARG_T TABLE(ACCOUNT_ID NUMBER)
)
RETURNS NUMBER
AS
$$
    SELECT COUNT_IF(ACCOUNT_ID IS NULL) FROM ARG_T
$$;

-- DMF: Credit score range validation (300-850)
CREATE OR REPLACE DATA METRIC FUNCTION GOVERNANCE.DMF_CREDIT_SCORE_RANGE(
    ARG_T TABLE(CREDIT_SCORE NUMBER(4,0))
)
RETURNS NUMBER
AS
$$
    SELECT COUNT_IF(CREDIT_SCORE < 300 OR CREDIT_SCORE > 850) FROM ARG_T
$$;

-- DMF: Email format validation (basic @ check)
CREATE OR REPLACE DATA METRIC FUNCTION GOVERNANCE.DMF_EMAIL_FORMAT(
    ARG_T TABLE(EMAIL VARCHAR)
)
RETURNS NUMBER
AS
$$
    SELECT COUNT_IF(EMAIL IS NOT NULL AND EMAIL NOT LIKE '%_@_%.__%') FROM ARG_T
$$;

-- DMF: Negative balance detection
CREATE OR REPLACE DATA METRIC FUNCTION GOVERNANCE.DMF_NEGATIVE_BALANCE(
    ARG_T TABLE(BALANCE NUMBER(15,2))
)
RETURNS NUMBER
AS
$$
    SELECT COUNT_IF(BALANCE < 0) FROM ARG_T
$$;

-- DMF: Future transaction date detection
CREATE OR REPLACE DATA METRIC FUNCTION GOVERNANCE.DMF_FUTURE_TXN_DATE(
    ARG_T TABLE(TXN_DATE TIMESTAMP_NTZ)
)
RETURNS NUMBER
AS
$$
    SELECT COUNT_IF(TXN_DATE > CURRENT_TIMESTAMP()) FROM ARG_T
$$;

-- Schedule DMFs on tables (run every 60 minutes)
ALTER TABLE BASE.TRANSACTIONS SET DATA_METRIC_SCHEDULE = '60 MINUTE';
ALTER TABLE BASE.CUSTOMERS SET DATA_METRIC_SCHEDULE = '60 MINUTE';
ALTER TABLE BASE.ACCOUNTS SET DATA_METRIC_SCHEDULE = '60 MINUTE';

-- Attach DMFs to specific columns
ALTER TABLE BASE.TRANSACTIONS
    ADD DATA METRIC FUNCTION GOVERNANCE.DMF_NULL_ACCOUNT_ID ON (ACCOUNT_ID);
ALTER TABLE BASE.TRANSACTIONS
