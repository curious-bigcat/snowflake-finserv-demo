-- ============================================================
-- 10. GOVERNANCE DASHBOARD SUMMARY
--     Aggregate governance posture into a single table.
-- ============================================================

SELECT '>>> GOVERNANCE SUMMARY <<<' AS SECTION;

CREATE OR REPLACE TABLE CONSUMPTION.GOVERNANCE_SUMMARY AS

-- Tag coverage: count of classified vs total columns
WITH tag_coverage AS (
    SELECT
        COUNT(DISTINCT COLUMN_NAME) AS TAGGED_COLUMNS
    FROM SNOWFLAKE.ACCOUNT_USAGE.TAG_REFERENCES
    WHERE TAG_NAME = 'DATA_CLASSIFICATION'
      AND OBJECT_DATABASE = 'FINSERV_DB'
      AND DOMAIN = 'COLUMN'
),
total_columns AS (
    SELECT COUNT(*) AS TOTAL_COLUMNS
    FROM FINSERV_DB.INFORMATION_SCHEMA.COLUMNS
    WHERE TABLE_SCHEMA IN ('BASE', 'RAW', 'CURATED', 'CONSUMPTION')
),
policy_count AS (
    SELECT
        COUNT(DISTINCT CASE WHEN POLICY_KIND = 'MASKING_POLICY' THEN REF_COLUMN_NAME END) AS MASKED_COLUMNS,
        COUNT(DISTINCT CASE WHEN POLICY_KIND = 'ROW_ACCESS_POLICY' THEN REF_ENTITY_NAME END) AS RAP_TABLES
    FROM SNOWFLAKE.ACCOUNT_USAGE.POLICY_REFERENCES
    WHERE REF_DATABASE_NAME = 'FINSERV_DB'
)

SELECT
    CURRENT_TIMESTAMP()                                           AS ASSESSED_AT,
    tc.TAGGED_COLUMNS                                             AS CLASSIFIED_COLUMNS,
    tot.TOTAL_COLUMNS                                             AS TOTAL_COLUMNS,
    ROUND(tc.TAGGED_COLUMNS / NULLIF(tot.TOTAL_COLUMNS, 0) * 100, 1) AS CLASSIFICATION_PCT,
    pc.MASKED_COLUMNS                                             AS MASKED_COLUMN_COUNT,
    pc.RAP_TABLES                                                 AS ROW_ACCESS_TABLE_COUNT,
    5                                                             AS CUSTOM_ROLE_COUNT,
    4                                                             AS TAG_COUNT,
    5                                                             AS DMF_COUNT
FROM tag_coverage tc, total_columns tot, policy_count pc;

SELECT * FROM CONSUMPTION.GOVERNANCE_SUMMARY;

