-- ============================================================
-- 8. VERIFY
-- ============================================================

-- Landing table counts (from COPY INTO)
SELECT 'CUSTOMERS_S3'        AS TABLE_NAME, COUNT(*) AS ROW_COUNT FROM CUSTOMERS_S3
UNION ALL SELECT 'TRANSACTIONS_S3',    COUNT(*) FROM TRANSACTIONS_S3
UNION ALL SELECT 'RISK_ASSESSMENTS_S3', COUNT(*) FROM RISK_ASSESSMENTS_S3
ORDER BY TABLE_NAME;

-- BASE table counts (after MERGE — should include CSV-sourced rows)
SELECT 'BASE.CUSTOMERS'        AS TABLE_NAME, COUNT(*) AS ROW_COUNT FROM BASE.CUSTOMERS
UNION ALL SELECT 'BASE.TRANSACTIONS',    COUNT(*) FROM BASE.TRANSACTIONS
UNION ALL SELECT 'BASE.RISK_ASSESSMENTS', COUNT(*) FROM BASE.RISK_ASSESSMENTS
ORDER BY TABLE_NAME;
