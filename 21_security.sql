-- Row access policy
CREATE OR REPLACE ROW ACCESS POLICY BASE.RAP_CUSTOMER_COUNTRY
    AS (country VARCHAR) RETURNS BOOLEAN ->
    CASE
        WHEN IS_ROLE_IN_SESSION('ACCOUNTADMIN') THEN TRUE
