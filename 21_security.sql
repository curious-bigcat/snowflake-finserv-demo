SELECT '>>> ROW ACCESS POLICY: SUPPORT TICKETS <<<' AS SECTION;

-- Mapping table: which user handles which agent name
CREATE TABLE IF NOT EXISTS RAW.SUPPORT_AGENT_MAP (
    SNOWFLAKE_USER  VARCHAR(100) NOT NULL,
    AGENT_NAME      VARCHAR(50)  NOT NULL,
    CONSTRAINT PK_AGENT_MAP PRIMARY KEY (SNOWFLAKE_USER)
);

