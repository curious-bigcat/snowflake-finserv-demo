SELECT 'DEMO_SUPPORT_2', 'Card Services'
WHERE NOT EXISTS (SELECT 1 FROM RAW.SUPPORT_AGENT_MAP WHERE SNOWFLAKE_USER = 'DEMO_SUPPORT_2');

-- Row access policy
CREATE OR REPLACE ROW ACCESS POLICY BASE.RAP_SUPPORT_TICKETS
    AS (assigned_to VARCHAR) RETURNS BOOLEAN ->
    CASE
        WHEN IS_ROLE_IN_SESSION('ACCOUNTADMIN') THEN TRUE
        WHEN IS_ROLE_IN_SESSION('FINSERV_ADMIN') THEN TRUE
        WHEN IS_ROLE_IN_SESSION('FINSERV_COMPLIANCE_OFFICER') THEN TRUE
        WHEN IS_ROLE_IN_SESSION('FINSERV_SUPPORT_AGENT')
            THEN EXISTS (
                SELECT 1 FROM RAW.SUPPORT_AGENT_MAP
                WHERE SNOWFLAKE_USER = CURRENT_USER()
                  AND AGENT_NAME = assigned_to
            )
        ELSE FALSE
    END;

ALTER TABLE BASE.SUPPORT_TICKETS
    ADD ROW ACCESS POLICY BASE.RAP_SUPPORT_TICKETS ON (ASSIGNED_TO);

SELECT 'Applied row access policy to SUPPORT_TICKETS' AS STATUS;


