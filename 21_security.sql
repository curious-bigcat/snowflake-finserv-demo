-- ============================================================
-- 10. SECURITY AUDIT SUMMARY
--     Query account usage for a security posture snapshot.
-- ============================================================

SELECT '>>> SECURITY AUDIT SUMMARY <<<' AS SECTION;

-- All custom roles and their grants
SELECT GRANTEE_NAME AS ROLE_NAME, GRANTED_ON, NAME AS GRANTED_OBJECT, PRIVILEGE
FROM SNOWFLAKE.ACCOUNT_USAGE.GRANTS_TO_ROLES
WHERE GRANTEE_NAME LIKE 'FINSERV%'
  AND DELETED_ON IS NULL
ORDER BY GRANTEE_NAME, GRANTED_ON, NAME;

-- All masking policies in FINSERV_DB
SELECT POLICY_NAME, POLICY_KIND, REF_ENTITY_NAME, REF_COLUMN_NAME
FROM SNOWFLAKE.ACCOUNT_USAGE.POLICY_REFERENCES
WHERE REF_DATABASE_NAME = 'FINSERV_DB'
  AND POLICY_KIND IN ('MASKING_POLICY', 'ROW_ACCESS_POLICY')
ORDER BY POLICY_KIND, REF_ENTITY_NAME;

-- Recent login history (last 24h)
SELECT USER_NAME, CLIENT_IP, REPORTED_CLIENT_TYPE,
       FIRST_AUTHENTICATION_FACTOR, IS_SUCCESS, ERROR_MESSAGE,
       EVENT_TIMESTAMP
FROM SNOWFLAKE.ACCOUNT_USAGE.LOGIN_HISTORY
WHERE EVENT_TIMESTAMP >= DATEADD('hour', -24, CURRENT_TIMESTAMP())
ORDER BY EVENT_TIMESTAMP DESC
LIMIT 20;

-- Failed login attempts (security monitoring)
SELECT USER_NAME, CLIENT_IP, ERROR_CODE, ERROR_MESSAGE,
       COUNT(*) AS FAILURE_COUNT
FROM SNOWFLAKE.ACCOUNT_USAGE.LOGIN_HISTORY
WHERE IS_SUCCESS = 'NO'
  AND EVENT_TIMESTAMP >= DATEADD('day', -7, CURRENT_TIMESTAMP())
GROUP BY USER_NAME, CLIENT_IP, ERROR_CODE, ERROR_MESSAGE
ORDER BY FAILURE_COUNT DESC;

