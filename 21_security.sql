-- ============================================================
-- 5. DYNAMIC DATA MASKING — PII COLUMNS
--    Mask names, email, phone, DOB, income, credit score.
--    Only FINSERV_ADMIN and FINSERV_COMPLIANCE_OFFICER see real data.
-- ============================================================

SELECT '>>> PII MASKING POLICIES <<<' AS SECTION;

-- Masking policy: VARCHAR (names, email, phone)
CREATE OR REPLACE MASKING POLICY BASE.MASK_PII_STRING AS (val VARCHAR)
RETURNS VARCHAR ->
    CASE
        WHEN IS_ROLE_IN_SESSION('FINSERV_ADMIN') THEN val
        WHEN IS_ROLE_IN_SESSION('FINSERV_COMPLIANCE_OFFICER') THEN val
        WHEN IS_ROLE_IN_SESSION('ACCOUNTADMIN') THEN val
        ELSE '***MASKED***'
    END;

-- Masking policy: DATE (date of birth)
CREATE OR REPLACE MASKING POLICY BASE.MASK_PII_DATE AS (val DATE)
RETURNS DATE ->
    CASE
        WHEN IS_ROLE_IN_SESSION('FINSERV_ADMIN') THEN val
        WHEN IS_ROLE_IN_SESSION('FINSERV_COMPLIANCE_OFFICER') THEN val
        WHEN IS_ROLE_IN_SESSION('ACCOUNTADMIN') THEN val
        ELSE DATE_FROM_PARTS(YEAR(val), 1, 1)  -- Redact to Jan 1 of birth year
    END;

-- Masking policy: NUMBER — income/credit score
CREATE OR REPLACE MASKING POLICY BASE.MASK_PII_NUMBER AS (val NUMBER(12,2))
RETURNS NUMBER(12,2) ->
    CASE
        WHEN IS_ROLE_IN_SESSION('FINSERV_ADMIN') THEN val
        WHEN IS_ROLE_IN_SESSION('FINSERV_COMPLIANCE_OFFICER') THEN val
        WHEN IS_ROLE_IN_SESSION('ACCOUNTADMIN') THEN val
        ELSE -1  -- Sentinel value indicating masked
    END;

-- Masking policy: NUMBER(4,0) — credit score specifically
CREATE OR REPLACE MASKING POLICY BASE.MASK_CREDIT_SCORE AS (val NUMBER(4,0))
RETURNS NUMBER(4,0) ->
    CASE
        WHEN IS_ROLE_IN_SESSION('FINSERV_ADMIN') THEN val
        WHEN IS_ROLE_IN_SESSION('FINSERV_COMPLIANCE_OFFICER') THEN val
        WHEN IS_ROLE_IN_SESSION('ACCOUNTADMIN') THEN val
        ELSE 0
    END;

-- Apply PII masking to CUSTOMERS table
ALTER TABLE BASE.CUSTOMERS MODIFY COLUMN FIRST_NAME    SET MASKING POLICY BASE.MASK_PII_STRING;
ALTER TABLE BASE.CUSTOMERS MODIFY COLUMN LAST_NAME     SET MASKING POLICY BASE.MASK_PII_STRING;
ALTER TABLE BASE.CUSTOMERS MODIFY COLUMN EMAIL         SET MASKING POLICY BASE.MASK_PII_STRING;
ALTER TABLE BASE.CUSTOMERS MODIFY COLUMN PHONE         SET MASKING POLICY BASE.MASK_PII_STRING;
ALTER TABLE BASE.CUSTOMERS MODIFY COLUMN DATE_OF_BIRTH SET MASKING POLICY BASE.MASK_PII_DATE;
ALTER TABLE BASE.CUSTOMERS MODIFY COLUMN ANNUAL_INCOME SET MASKING POLICY BASE.MASK_PII_NUMBER;
ALTER TABLE BASE.CUSTOMERS MODIFY COLUMN CREDIT_SCORE  SET MASKING POLICY BASE.MASK_CREDIT_SCORE;

SELECT 'Applied 7 masking policies to CUSTOMERS PII columns' AS STATUS;


